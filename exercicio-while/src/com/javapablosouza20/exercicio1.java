//1 - Crie um programa que imprima na tela a mensagem “Bom dia” 15 vezes, usando o laço enquanto
package com.javapablosouza20;

public class exercicio1 {
    public static void main(String[] args) {

        int i = 0;

        while (i < 15) {
            System.out.println(" BOM DIA = " + i);
            i += 1;
        }
    }
}
